package object2;

public class Printer {
	public int println(int n1) {
		System.out.println(n1);
		return n1;
		
	}
	
	public boolean println(boolean n1) {
		System.out.println(n1);
		return n1;
	}
	
	public double println(double n1) {
		System.out.println(n1);
		return n1;
	}
	
	public String println(String n1) {
		System.out.println(n1);
		return n1;
	}
	
	
}
